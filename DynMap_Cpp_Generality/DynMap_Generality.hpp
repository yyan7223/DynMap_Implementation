//////////////////////////////////////////// some common classes ////////////////////////////////////////////
class Tile{
    public:
        int ID;   
        int X;  
        int Y; 
        Tile(){};
        Tile(int id, int x, int y){
            ID = id;
            X = x;
            Y = y;
        }
        bool operator ==(const Tile& t)
        {
            return (this->ID == t.ID) && (this->X == t.X) && (this->Y == t.Y);
        }
};

class CGRA{
    public:
        int size;
        vector<Tile> Tiles;
        CGRA(){};
        CGRA(int size){
            size = size;
            int ID = 0;
            for(int i = 0; i < size; i++){
                for(int j = 0; j < size; j++){
                    Tiles.push_back(Tile(ID, j, i));
                    ID++;
                }
            }
        }
};

